//DB资源占用情况@MDK 3.80A@2.0版本：
//FLASH:4K~K字节(通过DB_USE_HELP和DB_USE_WRFUNS设置)
//SRAM:72字节(最少的情况下)
//SRAM计算公式:   SRAM=PARM_LEN+72-4  其中PARM_LEN必须大于等于4.
//应该保证堆栈不小于100个字节.
////////////////////////////////////////////用户配置参数////////////////////////////////////////////////////	  
#define MAX_DB_FNAME_LEN 		25	//函数名最大长度，应该设置为不小于最长函数名的长度。											   
#define MAX_DB_PARM_NO 			6	//最大为10个参数 ,修改此参数,必须修改db_exe与之对应.
#define MAX_DB_LEN 			32	//所有参数之和的长度不超过PARM_LEN个字节,注意串口接收部分要与之对应(不小于PARM_LEN),24l01接收1包最大为32字节


#define DB_ENTIMX_SCAN 	0	//使用TIM的定时中断来扫描SCAN函数,如果设置为0,需要自己实现隔一段时间扫描一次scan函数.
								//注意:如果要用runtime统计功能,必须设置DB_ENTIMX_SCAN为1!!!!
								
#define DB_USE_HELP		1	//使用帮助，该值设为0，可以节省近700个字节，但是将导致无法显示帮助信息。
#define DB_USE_WRFUNS	1	//使用读写函数,使能这里,可以读取任何地址的值,还可以写寄存器的值.
///////////////////////////////////////////////END///////////////////////////////////////////////////////////

#define DB_OK 			0  //无错误
#define DB_FUNCERR 		1  //函数错误
#define DB_PARMERR 		2  //参数错误
#define DB_PARMOVER 	3  //参数溢出
#define DB_NOFUNCFIND 	4  //未找到匹配函数

#define SP_TYPE_DEC      	0  //10进制参数显示
#define SP_TYPE_HEX       	1  //16进制参数显示


 //函数名列表	 
struct _m_db_nametab
{
	void* func;			//函数指针
	const u8* name;		//函数名(查找串)	 
};
//db控制管理器
struct _m_db_dev
{
	struct _m_db_nametab *funs;	//函数名指针

	void (*init)(u8);				//初始化
	u8 (*cmd_rec)(u8 *str);			//识别函数名及参数
	void (*exe)(void); 				//执行 
	void (*scan)(u8 *str);             //扫描
	u8 fnum; 				  		//函数数量
	u8 pnum;                        //参数数量
	u8 id;							//函数id
	u8 sptype;						//参数显示类型(非字符串参数):0,10进制;1,16进制;
	u16 parmtype;					//参数的类型
	u8  plentbl[MAX_DB_PARM_NO];  		//每个参数的长度暂存表
	u8  parm[MAX_DB_LEN];  			//函数的参数
	u8 runtimeflag;					//0,不统计函数执行时间;1,统计函数执行时间,注意:此功能必须在DB_ENTIMX_SCAN使能的时候,才有用
	u32 runtime;					//运行时间,单位:0.1ms,最大延时时间为定时器CNT值的2倍*0.1ms
};
extern struct _m_db_nametab db_nametab[];	//在db_config.c里面定义
extern struct _m_db_dev db_dev;				//在db_config.c里面定义


void db_init(u8 sysclk);//初始化
u8 db_cmd_rec(u8 *str);	//识别
void db_exe(void);		//执行
void db_scan(u8 *str);     //扫描


